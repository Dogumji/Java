package com.green.day03.ch05;

public class MissionFor1 {
    public static void main(String[] args) {
        int sum = 1;

        for (int i =2 ; i<=10 ; i++ ) {
            //n = n * i;
            sum *= i;
        }
        System.out.println(sum);
        System.out.println("------");
        System.out.printf("%,d\n",sum); // 정수 %,d = %d데 %,d 를 넣으면 =(3,628,800) 나옴







    }

}
